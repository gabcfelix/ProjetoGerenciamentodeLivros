    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = jTextField1.getText().trim();
        char[] passwordArray = jPasswordField1.getPassword();
        String password = new String(passwordArray).trim();

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nome de usuário e senha não podem estar vazios!");
            return;
        }

        String userType = ConexaoBancoDeDados.isValidUser(username, password);

        if (userType != null) {
            if ("admin".equals(userType)) {

                FormularioCadastroUsuario userRegistrationForm = new FormularioCadastroUsuario();
                userRegistrationForm.setVisible(true);
                dispose();
            } else if ("comum".equals(userType)) {

                FormularioOpcoesUsuarioComum opcoesUsuarioComumForm = new FormularioOpcoesUsuarioComum();
                opcoesUsuarioComumForm.setVisible(true);
                dispose();
            }
        } else {
                       JOptionPane.showMessageDialog(null, "Usuário ou senha inválidos!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

